Diese Seite ist eine Sammlung der wichtigsten Bierballregeln:
<br/><br/>REGELN
<br/><br/>SPIEL
<br/>Sternburg Export ist offizielles Bier. Anderes Bier ist zwar auch erlaubt, aber ungern gesehen. Wer mit Radler spielen will, braucht einen guten Grund. (+Muttizettel)
<br/>Geworfen wird mit und auf Flaschen.
<br/>Schnicken (Best of 3) zu Beginn. Wer Ehre hat, macht erste Runde Stein.
<br/>Make it – Take it. Limit ist 3. | auf Französisch: "le faire prendre" | Latein: "eam accipere"
<br/>Immer von oben werfen.
<br/>Es ist keine Schande ins Bier zu kotzen, solange man danach weitertrinkt.
<br/>Pro Spiel muss es 1 unparteiischen Schiedsrichter*in geben.
<br/>Schiedsrichter*innen nerven bitte nicht.
<br/>Alle Biere, außer das des/der Werfer*s*in, müssen während des Werfens auf dem Boden stehen. Losgelaufen werden darf ab dem Wurf. Aufgestellt werden darf erst ab dem Umfallen (Flasche nicht am Umfallen hindern)
<br/><br/>TEAMS
<br/>Teams bestehen aus genau drei Spieler*innen.
<br/>Tritt Team A nur zu zweit an (z.B. im Krankheitsfall), müssen aus Team B auch nur zwei Spieler*innen austrinken, um zu gewinnen.
<br/>Weniger als 2 Spieler*innen wären kein Team mehr, vong Anzahl her. Alleine spielen ist zwar auch erlaubt, aber dann muss der/die eine Spieler*in zwei Bier trinken.
<br/>Abwesende Spieler*innen können im Ausnahmefall vertreten werden, wenn das Gegnerteam einverstanden ist.
<br/>Die Zahl der geleerten Flaschen entscheidet bei Punktgleichstand. Danach der direkte Vergleich.
<br/><br/>WAS NOCH GESAGT WERDEN MUSS
<br/>Bierreste (Tropfen/Schaum) müssen über dem Kopf entleert werden.
<br/>Loserbier – Ex oder Nazi*in.
<br/>Biere die umkippen, werden ersetzt.
<br/>Überschäumen, zu lange trinken, etc. gibt Strafschlücke.
<br/>Beide Füße stehen hinter der Linie.
<br/>Menschen die raus sind dürfen noch laufen & werfen.
<br/>Wer wegkippt oder "Teambesprechung" macht, sorgt für die Niederlage seines/ihres Teams.
<br/>Ob auf Rasen oder Beton gespielt wird, machen sich die Teams untereinander aus. Im Zweifel wird geschnickt.
<br/>Telefonjoker sind nur auf Beton erlaubt.
<br/>Als alternative Bezeichnung zu "Bierball" ist "Flunkyball" oder auch die Mischung aus beidem, also "Blankiball", zugelassen.
